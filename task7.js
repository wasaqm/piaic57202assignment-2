var first = +prompt("Enter First Number")
var opr = prompt("Enter Operation")
var sec = +prompt("Enter Second Number")
if (opr == "+"){
    alert(first + sec)
}
if (opr == "-"){
    alert(first - sec)
}
if (opr == "*"){
    alert(first * sec)
}
if (opr == "/"){
    alert(first / sec)
}
if (opr == "%"){
    alert(first % sec)
}